    private String[] dameRetomar(){
        sql="SELECT nro_asiento FROM asientos where registrado='0' GROUP BY nro_asiento";  
        miSentencia=bd.dameSentencia();   
        try{
            ResultSet rs=miSentencia.executeQuery(sql);
            rs.last();
            int numFils =rs.getRow();
            retomar=new String[numFils];
            int i=0;
            rs.beforeFirst();
            while(rs.next()){
                
                retomar[i]=rs.getString("nro_asiento");
                i++;
                
            }
            
        }catch(Exception e){
            
        }
        return retomar;
    }
    
