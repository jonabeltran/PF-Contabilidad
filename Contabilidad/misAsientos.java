    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String[] arregloRetoma=dameRetomar();
        JComboBox comboRetoma=new JComboBox(arregloRetoma);
       
        String[] options = { "OK", "Cancel" };

        String title = "Asientos";
        int selection = JOptionPane.showOptionDialog(null, comboRetoma, title,
            JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null,
            options, options[0]); 
        
         if (selection == 0) {
             limpiarTabla(jTable1);
             String eleccion=comboRetoma.getSelectedItem().toString();
             String consultaSQL="select nro_asiento,DATE_FORMAT(fecha_contable, '%d-%m-%Y')AS fecha_contable,tipo_asiento,ok_carga,registrado,nro_renglon,nro_cuenta,descripcion,comprobante,sucursal,seccion,debe_haber,importe,leyenda from asientos where nro_asiento='"+eleccion+"'";
             Statement con=bd.dameSentencia();
             try{
                 ResultSet result=con.executeQuery(consultaSQL);
                 result.next();
                 jTextField5.setText(result.getString("nro_asiento"));
                 jFormattedTextField1.setText(result.getString("fecha_contable")); 
                 if(result.getString("tipo_asiento").equals("1")){
                      jLabel19.setText("Apertura");
                 }else if(result.getString("tipo_asiento").equals("5")){
                      jLabel19.setText("Normal");
                 }else{
                     jLabel19.setText("Cierre");
                 }
                 if(result.getString("ok_carga").equals("0")){
                      jLabel21.setText("Mal");
                 }else{
                      jLabel21.setText("Bien");
                 }
                 if(result.getString("registrado").equals("0")){
                      jLabel23.setText("No Registrado");
                 }else if(result.getString("registrado").equals("5")){
                      jLabel23.setText("En Proceso");
                 }else{
                      jLabel23.setText("Registrado");
                 }
                 ArrayList<Object[]> data=new ArrayList<>();
                 result.beforeFirst();
                 while(result.next()){
                     Object[] rows=new Object[9];
                     for(int i=0;i<rows.length;i++){
                         switch(i){
                             case 0:rows[i]=result.getObject("nro_renglon");
                                 break;
                             case 1:rows[i]=result.getObject("nro_cuenta");
                                 break;
                             case 2:rows[i]=result.getObject("descripcion");
                                 break;
                             case 3:rows[i]=result.getObject("comprobante");
                                 break;
                             case 4:rows[i]=result.getObject("sucursal");
                                 break;
                             case 5:rows[i]=result.getObject("seccion");
                                 break;
                             case 6:if(result.getString("debe_haber").equals("0")){
                                        rows[i]="Debe";
                                    }else{
                                        rows[i]="Haber";
                                    }   
                                 break;
                             case 7:rows[i]=result.getObject("importe");
                                 break;
                             case 8:rows[i]=result.getObject("leyenda");
                                 break;
                         }
                     }
                     data.add(rows);
                 }
                 DefaultTableModel md=(DefaultTableModel)this.jTable1.getModel();
                 for(int i=0;i<data.size();i++){
                     md.addRow(data.get(i));
                 }
             
             }catch(Exception e){
                 
             }
             
             String maxi="SELECT MAX(nro_renglon) as maxi FROM asientos WHERE nro_asiento='"+eleccion+"'";
             Statement maxist=bd.dameSentencia();
             try{
                 ResultSet maxirt=maxist.executeQuery(maxi);
                 maxirt.next();
                 int renglon=Integer.parseInt(maxirt.getString("maxi"))+1;
                 jTextField1.setText(String.valueOf(renglon));
                 jTextField2.requestFocus();
             }catch(Exception e){
                 
             }
         }
        
    }//GEN-LAST:event_jButton3ActionPerformed

