    private void jFormattedTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextField1KeyPressed
         jFormattedTextField1.setFocusTraversalKeys(KeyboardFocusManager.FORWARD_TRAVERSAL_KEYS,java.util.Collections.EMPTY_SET);
        if (evt.getKeyCode() == KeyEvent.VK_TAB) {
            
            SimpleDateFormat sd=new SimpleDateFormat("dd-MM-yyyy");
            try{
             Date dato1=sd.parse(jLabel3.getText());
             Date dato2=sd.parse(jLabel4.getText());
             Date dato3=sd.parse(jFormattedTextField1.getText());
           if(((dato1.before(dato3) || dato1.equals(dato3)) && (dato2.after(dato3) || dato2.equals(dato3)))  ){
            if(dato1.equals(dato3)){
                jLabel19.setText("Apertura");
            }else if(dato2.equals(dato3)){
                jLabel19.setText("Cierre");
            }else{
                jLabel19.setText("Normal");
            }
             jTextField5.requestFocus();
            }else{
               JOptionPane.showMessageDialog(null, "Fecha Incorrecta");
               jFormattedTextField1.setText("");
           }
            }catch(Exception e){
                
            }    
            
            int fila = jTable1.getRowCount();
            int ant=1;
            if(fila>0){
                for (int i = 0; i < fila; i++) {
                    if(ant<Integer.parseInt(jTable1.getValueAt(i, 0).toString())){
                       ant=Integer.parseInt(jTable1.getValueAt(i, 0).toString());
                    }
                }
                ant=ant+1;
            }
            jTextField1.setText(Integer.toString(ant));
            jTextField2.requestFocus();
        }
    }//GEN-LAST:event_jFormattedTextField1KeyPressed

