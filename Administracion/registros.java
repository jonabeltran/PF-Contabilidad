 private static void seleccionarPerfil(String s, JComboBox combo) {
    for (int i=0; i<combo.getItemCount(); i++) {
      if (combo.getItemAt(i).toString().equals(s)) {
        combo.setSelectedIndex(i);
        break;
      }
    }
    return;
  }
