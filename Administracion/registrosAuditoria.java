    public class itemSeleccionado implements ItemListener{
       
        @Override
        public void itemStateChanged(ItemEvent e) {
            
             if (e.getStateChange() == ItemEvent.SELECTED) {
                    // Aqui es donde se escribe el manejador del event        
                 try {
                     String desde=jComboBox1.getSelectedItem().toString();
                     
                     if(desde=="Todos"){
                         
                          mod=metodos.dameModelo("auditorArticulo");
        
                          jTable1.setModel(mod);
                         
                     }else{
                         
                     DefaultTableModel md=(DefaultTableModel)jTable1.getModel();
        
                     jTable1.setModel(md);
                     
                     SimpleDateFormat formato=new SimpleDateFormat("yyyy-MM-dd");
                     
                     Date date1=formato.parse(desde); 
                     
                     int i=0;
                     int tope=jTable1.getRowCount();
                     while(i<tope){
                         
                       String comparar=jTable1.getValueAt(i, 3).toString();
                    
                       Date date2=formato.parse(comparar);
                     
                       if(date2.before(date1)){
                         //JOptionPane.showMessageDialog(null, "esta despues");
                         md.removeRow(i);
                         tope=tope-1;
                       }else{
                           i++;
                       }
                         
                     }
                     }
                     
                 } catch (ParseException ex) {
                     Logger.getLogger(registrosAuditoria.class.getName()).log(Level.SEVERE, null, ex);
                 }
                 
             }
           
        }

     
    
    }
    
