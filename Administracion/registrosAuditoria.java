    public void insertaCombo(JTable tabla,JComboBox combo){
        
        combo.removeAllItems();
        
        combo.addItem("Todos");
        
         anterior=tabla.getValueAt(0, 3).toString();
        
        combo.addItem(anterior);
        
        for(int i=1;i<tabla.getRowCount();i++){
            
            siguiente=tabla.getValueAt(i, 3).toString();
            
            if(anterior.equals(siguiente)==false){
                
               combo.addItem(siguiente);
               
               anterior=siguiente;
                
            }
            
        }
      //  combo.setSelectedItem(null);
        
    }

